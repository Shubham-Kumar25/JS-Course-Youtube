const month = "Apr";

switch (month) {
  case "Jan":
    console.log("31 days in this month");
    break;
  case "Feb":
    console.log("28 or 29 days in this month");
    break;
  case "Mar":
    console.log("31 days in this month");
    break;
  default:
    console.log("30 or 31 days in this month");
    break;
}
